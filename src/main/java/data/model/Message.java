package data.model;

/**
 *
 * @author parrot
 */
public class Message {

    private String from;   // "user" hoặc "bot"
    private String text;

    public Message(String from, String text) {
        this.from = from;
        this.text = text;
    }

    public String getFrom() {
        return from;
    }

    public String getText() {
        return text;
    }
}
